# Store input numbers
num1 = input('Enter a number: ')
num2 = input('Enter a number: ')

# Add two numbers
sum = float(num1) + float(num2)
# Subtract two numbers
Sub = float(num1) - float(num2)
# Product two numbers
Multiply = float(num1) * float(num2)
# quotient two numbers
Divide = float(num1) / float(num2)

# Display the sum
print('The sum of {0} and {1} is {2}'.format(num1, num2, sum))
# Display the Sub
print('The sub of {0} and {1} is {2}'.format(num1, num2, Sub))
# Display the Product
print('The Product of {0} and {1} is {2}'.format(num1, num2, Multiply))
# Display the Quotient
print('The Quotient of {0} and {1} is {2}'.format(num1, num2, Divide))